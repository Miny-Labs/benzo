import { Keypair, Asset, TransactionBuilder, Operation, Horizon } from '@stellar/stellar-sdk';
import { writeFileSync, existsSync } from 'node:fs';
import { join } from 'node:path';
import { randomBytes } from 'node:crypto';

const HORIZON_URL = 'https://horizon-testnet.stellar.org';
const FRIENDBOT_URL = 'https://friendbot.stellar.org';
const USDC_CODE = 'USDC';
const USDC_ISSUER = 'GBBD47IF6LWK7P7MDEVSCWR7DPUWV3NY3DTQEVFL4NAT4AQH3ZLLFLA5';
const NETWORK_PASSPHRASE = 'Test SDF Network ; September 2015';

const outPath = '../../.env'; // Relative to apps/wallet-api

async function delay(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

async function fundWithFriendbot(publicKey, role) {
  console.log(`Funding ${role} (${publicKey}) via Friendbot...`);
  for (let i = 0; i < 3; i++) {
    try {
      const res = await fetch(`${FRIENDBOT_URL}?addr=${publicKey}`);
      if (res.ok) {
        console.log(`Successfully funded ${role}.`);
        return;
      }
      const text = await res.text();
      console.warn(`Friendbot attempt ${i + 1} failed: ${text}`);
    } catch (e) {
      console.warn(`Friendbot attempt ${i + 1} error: ${e.message}`);
    }
    await delay(3000);
  }
  throw new Error(`Failed to fund ${role} after 3 attempts.`);
}

async function addUsdcTrustline(secret, role) {
  const server = new Horizon.Server(HORIZON_URL);
  const keypair = Keypair.fromSecret(secret);
  const publicKey = keypair.publicKey();
  console.log(`Creating USDC trustline for ${role} (${publicKey})...`);
  
  const asset = new Asset(USDC_CODE, USDC_ISSUER);
  
  for (let i = 0; i < 5; i++) {
    try {
      const account = await server.loadAccount(publicKey);
      const tx = new TransactionBuilder(account, {
        fee: '10000',
        networkPassphrase: NETWORK_PASSPHRASE,
      })
        .addOperation(Operation.changeTrust({ asset }))
        .setTimeout(30)
        .build();
        
      tx.sign(keypair);
      await server.submitTransaction(tx);
      console.log(`USDC trustline created successfully for ${role}.`);
      return;
    } catch (e) {
      console.warn(`Trustline attempt ${i + 1} failed for ${role}: ${e.message}`);
      await delay(2000);
    }
  }
  throw new Error(`Failed to create trustline for ${role}`);
}

async function main() {
  if (existsSync(outPath)) {
    console.log(`Refusing to overwrite existing .env file at ${outPath}. Please delete it if you want to regenerate keys.`);
    process.exit(1);
  }

  console.log('Generating Stellar testnet keys...');
  const deployer = Keypair.random();
  const relayer = Keypair.random();
  const anchorDist = Keypair.random();
  const anchorSign = Keypair.random();

  // Fund all keys
  await fundWithFriendbot(deployer.publicKey(), 'deployer');
  await fundWithFriendbot(relayer.publicKey(), 'relayer');
  await fundWithFriendbot(anchorDist.publicKey(), 'anchor-dist');
  await fundWithFriendbot(anchorSign.publicKey(), 'anchor-sign');

  // Wait a bit for Horizon to ingest
  console.log('Waiting for Horizon to ingest funded accounts...');
  await delay(5000);

  // Add trustlines
  await addUsdcTrustline(deployer.secret(), 'deployer');
  await addUsdcTrustline(relayer.secret(), 'relayer');
  await addUsdcTrustline(anchorDist.secret(), 'anchor-dist');

  const relayerAuthToken = randomBytes(24).toString('hex');

  const envContent = `# Generated by setup-testnet-env.mjs — Stellar testnet
STELLAR_NETWORK=testnet
NETWORK_PASSPHRASE="${NETWORK_PASSPHRASE}"
SOROBAN_RPC_URL=https://soroban-testnet.stellar.org
HORIZON_URL=${HORIZON_URL}
FRIENDBOT_URL=${FRIENDBOT_URL}
USE_TEST_TOKEN=false
USDC_CODE=${USDC_CODE}
USDC_ISSUER=${USDC_ISSUER}

DEPLOYER_PUBLIC=${deployer.publicKey()}
DEPLOYER_SECRET=${deployer.secret()}
RELAYER_PUBLIC=${relayer.publicKey()}
RELAYER_SECRET=${relayer.secret()}
ANCHOR_DISTRIBUTION_PUBLIC=${anchorDist.publicKey()}
ANCHOR_DISTRIBUTION_SECRET=${anchorDist.secret()}
ANCHOR_SIGNING_PUBLIC=${anchorSign.publicKey()}
ANCHOR_SIGNING_SECRET=${anchorSign.secret()}
RELAYER_AUTH_TOKEN=${relayerAuthToken}
BENZO_ALLOWED_ORIGINS=http://localhost:5173,http://localhost:5175
`;

  writeFileSync(outPath, envContent, 'utf8');
  console.log(`Success! Environment file written to ${outPath}`);
}

main().catch((err) => {
  console.error('Setup failed:', err);
  process.exit(1);
});
